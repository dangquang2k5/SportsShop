    <script>
        // Page Loader
        window.addEventListener('load', function() {
            setTimeout(() => {
                const loader = document.getElementById('pageLoader');
                loader.style.opacity = '0';
                setTimeout(() => {
                    loader.style.display = 'none';
                }, 200);
                
                // Animate page elements
                animatePageElements();
            }, 150);
        });

        // Dark Mode Toggle
        function toggleDarkMode() {
            const html = document.documentElement;
            const isDark = html.classList.toggle('dark');
            localStorage.setItem('darkMode', isDark ? 'enabled' : 'disabled');
        }

        // Load Dark Mode Preference
        if (localStorage.getItem('darkMode') === 'enabled') {
            document.documentElement.classList.add('dark');
        }

        // Mobile Menu Toggle
        function toggleMobileMenu() {
            const menu = document.getElementById('mobileMenu');
            const overlay = document.getElementById('mobileMenuOverlay');
            const isActive = menu.classList.toggle('active');
            
            if (isActive) {
                overlay.classList.remove('hidden');
                document.body.style.overflow = 'hidden';
            } else {
                overlay.classList.add('hidden');
                document.body.style.overflow = '';
            }
        }

        // Scroll to Top
        function scrollToTop() {
            if (typeof anime !== 'undefined') {
                anime({
                    targets: [document.documentElement, document.body],
                    scrollTop: 0,
                    duration: 600,
                    easing: 'easeInOutQuad'
                });
            } else {
                window.scrollTo({ top: 0, behavior: 'smooth' });
            }
        }

        // Show/Hide Scroll Top Button
        window.addEventListener('scroll', function() {
            const scrollBtn = document.getElementById('scrollTopBtn');
            const nav = document.getElementById('mainNav');
            
            if (window.scrollY > 300) {
                scrollBtn.style.opacity = '1';
                scrollBtn.style.pointerEvents = 'auto';
                nav.classList.add('shadow-lg');
            } else {
                scrollBtn.style.opacity = '0';
                scrollBtn.style.pointerEvents = 'none';
                nav.classList.remove('shadow-lg');
            }
        });

        // Animate Page Elements on Load
        function animatePageElements() {
            // Check if anime.js is loaded
            if (typeof anime === 'undefined') {
                // Fallback to CSS animations
                document.querySelectorAll('.modern-card, section').forEach(el => {
                    el.style.opacity = '1';
                    el.style.transform = 'translateY(0)';
                });
                return;
            }
            
            // Animate cards
            const cards = document.querySelectorAll('.modern-card');
            anime({
                targets: cards,
                translateY: [30, 0],
                opacity: [0, 1],
                delay: anime.stagger(50, {start: 100}),
                duration: 400,
                easing: 'easeOutQuad'
            });

            // Animate sections
            const sections = document.querySelectorAll('section');
            anime({
                targets: sections,
                opacity: [0, 1],
                translateY: [20, 0],
                delay: anime.stagger(80),
                duration: 500,
                easing: 'easeOutQuad'
            });
        }

        // Smooth Scroll for Anchor Links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    if (typeof anime !== 'undefined') {
                        anime({
                            targets: [document.documentElement, document.body],
                            scrollTop: target.offsetTop - 80,
                            duration: 600,
                            easing: 'easeInOutQuad'
                        });
                    } else {
                        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
                    }
                }
            });
        });

        // Add hover animations to buttons (CSS handles this now)
        // Removed anime.js dependency for better performance

        // Intersection Observer for scroll animations
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('animate-slide-up');
                    observer.unobserve(entry.target);
                }
            });
        }, observerOptions);

        // Observe elements with animation class
        document.querySelectorAll('.animate-on-scroll').forEach(el => {
            observer.observe(el);
        });

        // Admin Sidebar Sticky Position Control
        function updateSidebarPosition() {
            const sidebar = document.getElementById('admin-sidebar');
            if (!sidebar) return;

            const footer = document.querySelector('footer');
            if (!footer) return;

            const sidebarHeight = sidebar.offsetHeight;
            const footerTop = footer.getBoundingClientRect().top;
            const windowHeight = window.innerHeight;
            const navHeight = 80;

            // If footer is visible in viewport
            if (footerTop < windowHeight) {
                // Calculate how much the sidebar should move up
                const overlap = windowHeight - footerTop;
                const maxScroll = sidebarHeight - (windowHeight - navHeight);
                
                // Move sidebar up to prevent overlap with footer
                if (overlap > 0) {
                    sidebar.style.maxHeight = `calc(100vh - 80px - ${overlap}px)`;
                } else {
                    sidebar.style.maxHeight = `calc(100vh - 80px - 20px)`;
                }
            } else {
                sidebar.style.maxHeight = `calc(100vh - 80px - 20px)`;
            }
        }

        // Update sidebar position on scroll and resize
        window.addEventListener('scroll', updateSidebarPosition);
        window.addEventListener('resize', updateSidebarPosition);
        
        // Initial call
        updateSidebarPosition();
    </script>
